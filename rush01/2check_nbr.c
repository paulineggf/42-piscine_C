int		check_nbr(int **tab, int x, int y, int n)
{
	if (tab[x][1] == n || tab[x][2] == n || tab[x][3] == n || tab[x][4] == n)
		return (0);
	if (tab[1][y] == n || tab[2][y] == n || tab[3][y] == n || tab[4][y] == n)
		return (0);
	return (1);
} 
