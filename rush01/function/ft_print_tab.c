void	ft_putchar(char c);

void	ft_print_tab(int **tab)
{
	int		x;
	int 	y;

	x = 1;
	while (x <= 4)
	{
		y = 1;
		while (y <= 4)
		{
			ft_putchar(tab[x][y] + 48);
			y++;
		}
		x++;
	}
}
