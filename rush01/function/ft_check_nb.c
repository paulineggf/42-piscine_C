int		ft_check_nb(int x, int y, int **tab, int nb)
{
	int		i;

	if (y > 0)
	{
		i = 1;
		while (i <= 4)
		{
			if (nb == tab[i][y])
				return (0);
			i++;
		}
	}
	if (x > 0)
	{
		i = 1;
		while (i <= nb)
		{
			if (nb == tab[x][y])
				return (0);
			i++;
		}
	}
	return (1);
}
