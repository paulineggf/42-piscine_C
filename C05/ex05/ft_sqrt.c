int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	if (nb < 0)
		return (0);
	if (nb > 10000000)
		i = 3000;
	if (nb > 100000000)
		i = 10000;
	while (i * i < nb && i < 46341)
		i++;
	if (i * i == nb)
		return (i);
	return (0);
}
