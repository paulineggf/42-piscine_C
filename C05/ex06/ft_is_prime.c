int		ft_is_prime(int nb)
{
	int		i;

	if ((nb < 2 || nb % 2 == 0) && nb != 2)
		return (0);
	if (nb == 2)
		return (1);
	i = 3;
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		if (nb % 2 == 0)
			i += 1;
		else
			i += 2;
	}
	return (1);
}
